package Controles;

public class Aula11_Break {
	public static void main(String[] args) {
		
		
		for(int i = 1; i <= 10; i++) {
			
			//Si i for maior que 5 para
			if( i > 5) {
			// o break para o laço e vai pro proximo
			//codigo, ele meio que quebra o fluxo de repetição.
				break;
			}
			
			System.out.println(i);
			
		}
		
	}
}

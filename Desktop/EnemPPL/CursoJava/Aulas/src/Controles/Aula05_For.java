package Controles;

public class Aula05_For {
	
	public static void main(String[] args) {
		

		/*Essa estrutura é planeja para ter uma quantidade
		 * determinada de repetições, ou seja iremos utilizar
		 * o for quando precisarmos de uma quantidade exata de
		 * repetições*/
		
		//para cada elemento em um conjunto, faça algo" ou seja
		//enquanto "c" for menor ou igual a 10 adicione +1 a "c"
		for(int c = 0; c <=10; c++) {
			System.out.println("C ="+" "+c);
			
			
		}
	}

}

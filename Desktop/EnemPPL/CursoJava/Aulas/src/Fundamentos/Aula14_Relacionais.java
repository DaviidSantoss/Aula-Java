package Fundamentos;

public class Aula14_Relacionais {
	
	public static void main(String[] args) {
		
		// == comparação
		System.out.println(3 == 5); //false, 3 não é igual a 5
		
		// > maior que
		System.out.println(3 > 2);//true, 3 é maior que 2
		
		// >= maior ou igual a 
		System.out.println(3 >= 3);//true, 3 é maior ou igual a 3
		
		// < menor que
		System.out.println(5<2);//false, 5 não é menor que 2
		
		// =< menor ou igual
		System.out.println(4<=4);//true, 4 é menor ou igual a 4
		
		// != diferente
		System.out.println(30 != 2);//true, 30 é diferente de 2
		
		
	}

}
